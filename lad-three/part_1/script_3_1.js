// Варіант 10. Обробка двох дійсних чисел x, y

let x = parseFloat(prompt("Введіть значення x:"));
let y = parseFloat(prompt("Введіть значення y:"));

if (x < 0 && y < 0) {
    // обидва від’ємні
    x = Math.abs(x);
    y = Math.abs(y);
} else if ((x < 0 && y >= 0) || (x >= 0 && y < 0)) {
    // від’ємне лише одне
    x += 0.5;
    y += 0.5;
} else if (x >= 0 && y >= 0 && !(x >= 0.5 && x <= 2.0) && !(y >= 0.5 && y <= 2.0)) {
    // обидва невід’ємні і жодне не в [0.5, 2.0]
    x /= 10;
    y /= 10;
} 
// в інших випадках залишаємо без змін

alert("Результати:\n x = " + x + "\n y = " + y);
